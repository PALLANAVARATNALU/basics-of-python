#  Python operators used to perform operations between values
# 1.Arithemetic Operator(+,-,*,%,/,//)
# 2.Assignment Operator(=,!=)
# 3.Comparison Operator (==,>=,<=,!=,>,<)
# 4.logical Operator (AND,OR,NOT)


# Arithemetic operator(+,-,*,/,%,//)

a=5
b=6
print(a+b)    # addition
print(a-b)    # subtraction
print(a*b)    # multiplication
print(a/b)    # division
print(a%b)    # modules
print(a//b)   #float division


# Assignment Operator(=,!=)
x=5
print(x)
# here x is variable
# = is assignment operator
# 5 is a value


# comparision operator(>,<,==,!=,>=,<=)
a=10
b=20
print(a==b)
print(a!=b)
print(a>=b)
print(a<=b)
print(a>b)
print(a<b)

# Logical Operator (AND,OR,NOT)
print(4 and 5)
print(4 or 5)
