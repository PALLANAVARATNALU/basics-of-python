# Dictionary is used to store pair of elements,stores keypair values

#example: print basic data as dictionary
dict1={"name":"rossom","place":"california","designation":"developer","salary":"40000"}
dict2={"name":"iyer","place":"punjab","designation":"captain","ismarried":False}
print(dict2["ismarried"])     # results false
print(dict2["name"])         # results iyer
print(dict2["place"])        # results punjab
print(dict2["designation"])  # results captain
print(dict1["name"])         # results rossom
print(dict1["place"])        # results california
print(dict1["designation"])  # results developer
print(dict1["salary"])      # results 40000



